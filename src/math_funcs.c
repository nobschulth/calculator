#include "math_funcs.h"
#include <math.h>

double op_add(double a, double b) {
    return a + b;
}

double op_sub(double a, double b) {
    return a - b;
}

double op_mul(double a, double b) {
    return a * b;
}

double op_div(double a, double b) {
    return a / b;
}

double op_pow(double a, double b) {
    return pow(a, b);
}
